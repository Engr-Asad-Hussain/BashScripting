#! /bin/bash   # Bash scripting starts with the location of bash

# echo will print following strings
echo "Asad Hussain NEDUET"

# Variables are called using $ symbol.
# There are two types of Variables. System Variables and Userdefined Variables.
# System Variables is always written in capital letters.
# Example following are system variables.
echo $BASH
echo $BASH_VERSION
echo $HOME
echo $PWD

# Example following are user defined variables.
name='Asad Hussain'
echo 'My name is' $name